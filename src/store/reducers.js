// reducers.js
const initialState = {
  fullName: '',
  email: '',
  mobileNumber: '',
  role: '',
  password: '',
  confirmPassword: '',
};

const signUpReducer = (state = initialState, action) => {
  switch (action.type) {
    case 'UPDATE_FIELD':
      return {
        ...state,
        [action.payload.field]: action.payload.value,
      };
    default:
      return state;
  }
};

